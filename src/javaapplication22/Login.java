    private void verificarLogin() {
        String usuario = jTextField1.getText();
        String contrasena = new String(jPasswordField1.getPassword());
        
         //usuarios.put("admin", new usuarios("Admin", "1234"));

        if (usuarios.containsKey(usuario) && usuarios.get(usuario).getContrasena().equals(contrasena)) {
            //JOptionPane.showMessageDialog(this, "Login exitoso", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false); // Ocultar el frame actual
            new MostrarUnidades().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Usuario o contraseña incorrectos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
